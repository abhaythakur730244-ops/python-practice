# String is data type stores a sequence of characters.

name = "Mark"
print(name)

# Basic string operations
print("Hello" + " " + "World") # Concatenation
print("Hello " * 3) # Repetition
print(len(name)) # Length of the string
print(name[0]) # Accessing characters (indexing)
print(name.split(name[2])) # Split the string by a character
print("a" in name) # Check if a character is in the string
print("z" not in name) # Check if a character is not in the string 