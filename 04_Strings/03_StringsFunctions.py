# Strings Functions
string = "Hello, World!"
print(string.upper()) # Convert to uppercase
print(string.lower()) # Convert to lowercase
print(string.capitalize()) # Capitalize the first character
print(string.replace("o", "0")) # Replace characters
print(string.split(", ")) # Split the string by a delimiter
print(string.strip("!")) # Remove characters from the ends
print(string.find("World")) # Find the index of a substring
print(string.count("o")) # Count occurrences of a character
print(string.startswith("Hello")) # Check if the string starts with a substring
print(string.endswith("!")) # Check if the string ends with a substring


