# Indexing and slicing examples

fruit = "Banana"

print(fruit[0]) # First character
print(fruit[-1]) # Last character
print(fruit[1:4]) # Substring from index 1 to 3
print(fruit[:3]) # Substring from start to index 2
print(fruit[3:]) # Substring from index 3 to end
print(fruit[::2]) # Substring with a step of 2
print(fruit[2:4]) # Substring from index 2 to 3
print(fruit[1:4:2]) # Substring from index 1 to 3 with a step of 2